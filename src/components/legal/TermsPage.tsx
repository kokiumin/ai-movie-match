import { useState } from "react";
import { LegalLayout, Section, OL, COMPANY_FOOTER } from "./LegalLayout";

type Tab = "client" | "creator";

export function TermsPage({ onBack, initialTab = "client" }: { onBack: () => void; initialTab?: Tab }) {
  const [tab, setTab] = useState<Tab>(initialTab);
  return (
    <LegalLayout
      title={tab === "client" ? "利用規約（発注者向け）" : "利用規約（クリエイター向け）"}
      subtitle="最終更新日: 2026年4月18日 / 発効日: 別途定める"
      onBack={onBack}
      footerNote={COMPANY_FOOTER}
    >
      {/* タブ切替 */}
      <div className="flex border-b border-gray-200 mb-6">
        {[
          { id: "client" as Tab, label: "発注者向け" },
          { id: "creator" as Tab, label: "クリエイター向け" },
        ].map((t) => (
          <button
            key={t.id}
            onClick={() => setTab(t.id)}
            className={`px-4 py-2.5 text-sm font-semibold border-b-2 -mb-px ${
              tab === t.id
                ? "border-blue-700 text-blue-700"
                : "border-transparent text-gray-500 hover:text-gray-800"
            }`}
          >
            {t.label}
          </button>
        ))}
      </div>

      {tab === "client" ? <ClientTerms /> : <CreatorTerms />}
    </LegalLayout>
  );
}

